import datetime
import time
print("Current date and time:",datetime.datetime.now())
print("Current year:",datetime.date.today().strftime("%y"))
print("Month of the year:",datetime.date.today().strftime("%B"))
print("Week number of the year:",datetime.date.today().strftime("%f"))
print("Day of the year:",datetime.date.today().strftime("%Y"))
print("Day of the week:",datetime.date.today().strftime("%A"))
print("Day of the month:",datetime.date.today().strftime("%C"))


